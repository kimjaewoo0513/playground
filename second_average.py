def calculate_average_time(seconds_list):
    total_seconds = sum(seconds_list)
    average_seconds = total_seconds / len(seconds_list)
    return average_seconds

def main():
    seconds_list = []
    while True:
        try:
            seconds = float(input("초를 입력하십시오 (종료하려면 0 입력): "))
            if seconds == 0:
                break
            seconds_list.append(seconds)
        except ValueError:
            print("유효한 숫자를 입력하십시오.")

    if seconds_list:
        average_time = calculate_average_time(seconds_list)
        print("입력된 초들의 평균 시간은 {:.2f}초입니다.".format(average_time))
    else:
        print("최소한 하나의 초를 입력해야 합니다.")

if __name__ == "__main__":
    main()

